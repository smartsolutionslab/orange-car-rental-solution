namespace SmartSolutionsLab.OrangeCarRental.Customers.Domain.Customer;

/// <summary>
///     Salutation enum for German market customer names.
///     Represents formal titles used in business correspondence.
/// </summary>
public enum Salutation
{
    /// <summary>
    ///     Herr (Mr.) - Male salutation
    /// </summary>
    Herr = 1,

    /// <summary>
    ///     Frau (Mrs./Ms.) - Female salutation
    /// </summary>
    Frau = 2,

    /// <summary>
    ///     Dr. - Doctor (academic or medical degree)
    /// </summary>
    Dr = 3,

    /// <summary>
    ///     Prof. - Professor (academic title)
    /// </summary>
    Prof = 4,

    /// <summary>
    ///     Prof. Dr. - Professor with doctorate
    /// </summary>
    ProfDr = 5
}
